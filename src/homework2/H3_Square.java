package homework2;

public class H3_Square {
        public static void main(String[] args) {

            System.out.println("Квадраты чисел от 10 до 20:");

            for (int i = 10; i <= 20; i++) {
                int square = i * i;  // вычисляем квадрат числа
                System.out.println(i + " в квадрате = " + square);
            }
        }
    }

